// AT07: Known inputs produce m unique indices in valid range.
func TestDeriveIndices_KnownInputsUniqueAndInRange(t *testing.T) {
	actionID := "cascade_test_001"
	prevBlockHash := []byte{
		0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77,
		0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff,
		0x10, 0x21, 0x32, 0x43, 0x54, 0x65, 0x76, 0x87,
		0x98, 0xa9, 0xba, 0xcb, 0xdc, 0xed, 0xfe, 0x0f,
	}
	height := uint64(12345)
	signer := []byte{
		0xab, 0xcd, 0xef, 0x01, 0x23, 0x45, 0x67, 0x89,
		0xab, 0xcd, 0xef, 0x01, 0x23, 0x45, 0x67, 0x89,
		0xab, 0xcd, 0xef, 0x01,
	}
	numChunks := uint32(1000)
	m := uint32(8)

	indices := DeriveIndices(actionID, prevBlockHash, height, signer, numChunks, m)
	require.Len(t, indices, int(m))

	// Deterministic vector generated from the normative formula in docs/new-feature-lep5.md (Blake3).
	require.Equal(t, []uint32{476, 34, 289, 159, 77, 46, 130, 569}, indices)

	seen := make(map[uint32]struct{}, m)
	for _, idx := range indices {
		require.Less(t, idx, numChunks)
		_, exists := seen[idx]
		require.False(t, exists, "duplicate index %d", idx)
		seen[idx] = struct{}{}
	}
}

// AT08: Same inputs produce same outputs; different signer changes outputs.
func TestDeriveIndices_DeterministicAndSignerBound(t *testing.T) {
	actionID := "cascade_test_001"
	prevBlockHash := []byte{
		0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77,
		0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff,
		0x10, 0x21, 0x32, 0x43, 0x54, 0x65, 0x76, 0x87,
		0x98, 0xa9, 0xba, 0xcb, 0xdc, 0xed, 0xfe, 0x0f,
	}
	height := uint64(12345)
	numChunks := uint32(1000)
	m := uint32(8)

	signerA := []byte{
		0xab, 0xcd, 0xef, 0x01, 0x23, 0x45, 0x67, 0x89,
		0xab, 0xcd, 0xef, 0x01, 0x23, 0x45, 0x67, 0x89,
		0xab, 0xcd, 0xef, 0x01,
	}
	signerB := []byte{
		0xde, 0xad, 0xbe, 0xef, 0x00, 0x11, 0x22, 0x33,
		0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb,
		0xcc, 0xdd, 0xee, 0xff,
	}

	indices1 := DeriveIndices(actionID, prevBlockHash, height, signerA, numChunks, m)
	indices2 := DeriveIndices(actionID, prevBlockHash, height, signerA, numChunks, m)
	require.Equal(t, indices1, indices2)

	indicesOtherSigner := DeriveIndices(actionID, prevBlockHash, height, signerB, numChunks, m)
	require.NotEqual(t, indices1, indicesOtherSigner)
}

